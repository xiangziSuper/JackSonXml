public class PortpmXml {

    private String ctpName;
    private String tpName;
    private String layerRate;
    private String performanceIndicator;

    public PortpmXml() {
    }

    public String getCtpName() {
        return this.ctpName;
    }

    public void setCtpName(String ctpName) {
        this.ctpName = ctpName;
    }

    public String getTpName() {
        return this.tpName;
    }

    public void setTpName(String tpName) {
        this.tpName = tpName;
    }

    public String getLayerRate() {
        return this.layerRate;
    }

    public void setLayerRate(String layerRate) {
        this.layerRate = layerRate;
    }

    public String getPerformanceIndicator() {
        return this.performanceIndicator;
    }

    public void setPerformanceIndicator(String performanceIndicator) {
        this.performanceIndicator = performanceIndicator;
    }
}
